def setup_session(sid, entries, queue_dir=None):
    """Register a session with given transcript entries."""
    server.sessions[sid] = {
        "transcript_path": "",
        "terminal_id": "",
        "tmux_socket": "",
        "cwd": "/tmp",
        "registered_at": time.time(),
        "transcript_offset": 0,
        "transcript_entries": entries,
        "derived_state": "idle",
        "last_activity": time.time(),
        "last_summary": "",
        "last_user_prompt": "",
    }


