class TestDeriveStateBusy:
    def test_user_after_assistant(self):
        entries = [
            make_assistant_entry("previous reply", stop_reason="end_turn"),
            make_user_entry("new question"),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "busy"
        assert summary == ""  # stale summary cleared

    def test_local_command_not_busy(self):
        """Local commands like /context should NOT make session busy."""
        local_cmd = (
            '<local-command-caveat>Caveat: messages below were generated by local commands.</local-command-caveat>\n'
            '<command-name>/context</command-name>\n'
            '<command-message>context</command-message>\n'
            '<command-args></command-args>\n'
            '<local-command-stdout>some output here</local-command-stdout>'
        )
        entries = [
            make_user_entry("hello"),
            make_assistant_entry("Hi there!", stop_reason="end_turn"),
            make_user_entry(local_cmd),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "idle"

    def test_local_command_with_real_text_is_busy(self):
        """A message with both local command tags AND real user text should be busy."""
        mixed = (
            '<local-command-caveat>Caveat</local-command-caveat>\n'
            '<command-name>/context</command-name>\n'
            '<local-command-stdout>output</local-command-stdout>\n'
            'but also a real question here'
        )
        entries = [
            make_assistant_entry("previous reply", stop_reason="end_turn"),
            make_user_entry(mixed),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "busy"

    def test_interrupt_is_idle(self):
        """After Ctrl-C, transcript has '[Request interrupted by user]' — should be idle."""
        entries = [
            make_assistant_entry("Working on it...", stop_reason="stop_sequence"),
            make_user_entry("[Request interrupted by user]"),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "idle"

    def test_interrupt_for_tool_use_is_idle(self):
        """Interrupt during tool use should also be idle."""
        entries = [
            make_assistant_entry(
                "Let me edit.",
                tool_uses=[make_tool_use("Edit", {}, "tu-99")],
                stop_reason="tool_use",
            ),
            make_user_entry("[Request interrupted by user for tool use]"),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "idle"

    def test_interrupt_with_no_stop_reason_is_idle(self):
        """Interrupt when assistant had no stop_reason (empty/None)."""
        entries = [
            make_assistant_entry("Partial response...", stop_reason=""),
            make_user_entry("[Request interrupted by user]"),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "idle"

    def test_unresolved_tool_use(self):
        entries = [
            make_user_entry("fix the bug"),
            make_assistant_entry(
                "Let me edit.",
                tool_uses=[make_tool_use("Edit", {}, "tu-99")],
                stop_reason="tool_use",
            ),
        ]
        setup_session("s1", entries)
        state, summary, prompt = server._derive_state("s1", server.sessions["s1"])
        assert state == "busy"


